package com.epam.se01.operators;

public class Example1 {

    public static void main(String[] args) {
        int value = 0;

        // 0
        System.out.println(++value);  // 1
        // 1

        // 1
        System.out.println(value++);  // 1
        // 2

        System.out.println(value);   // 2

    }
}
